// Arithmetic Operators

public class Program_5 {
    public static void main(String[] args) {


        int x = 10;
        int y = 2;
        int z;

        z = x + y; // ADDITION
        z = x - y; // SUBTRACTION
        z = x * y; // MULTIPLICATION
        z = x / y; // DIVISION
        z = x % y; // MODULUS

        System.out.println(z);

    }
}
